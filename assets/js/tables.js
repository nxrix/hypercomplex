const tables = [];

const gr = Array.from({ length: 4 }, () => Array(4).fill(-1));
const ru = Array.from({ length: 4 }, () => Array(4).fill(false));
const cu = Array.from({ length: 4 }, () => Array(4).fill(false));

const backtrack = (p) => {
  if (p===16) {
    tables.push(gr.map(r=>r.slice()).flat());
    return;
  }
  const r = Math.floor(p/4);
  const c = p%4;
  for (let s=0;s<4;s++) {
    if (!ru[r][s]&&!cu[c][s]) {
      gr[r][c] = s;
      ru[r][s] = true;
      cu[c][s] = true;
      backtrack(p+1);
      ru[r][s] = false;
      cu[c][s] = false;
      gr[r][c] = -1;
    }
  }
}
backtrack(0);

